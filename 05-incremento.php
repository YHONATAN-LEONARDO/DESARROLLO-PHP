<?php   // ini

$x = 5;          // var base

echo ++$x;       // pre-inc, imprime 6
echo $x;         // ahora vale 6

echo $x++;       // post-inc, imprime 6
echo $x;         // ahora vale 7

echo --$x;       // pre-dec, imprime 6
echo $x;         // ahora vale 6

echo $x--;       // post-dec, imprime 6
echo $x;         // ahora vale 5

?>  // fin
